class Colors:
    """
    Prints the ANSI escape sequences for different font colors to display
    in a user's terminal.
    """
    black =     '\033[30m'
    red1 =       '\033[31m'
    green1 =     '\033[32m'
    orange =    '\033[33m'
    blue1 =      '\033[34m'
    purple =    '\033[35m'
    cyan1 =      '\033[36m'
    darkgrey =  '\033[90m'
    red =       '\033[91m'
    green =     '\033[92m'
    yellow =    '\033[93m'
    blue =      '\033[94m'
    magenta =   '\033[95m'
    cyan =      '\033[96m'
    lightgrey = '\033[97m'
    end =       '\033[00m'
    bold =      '\033[1m'
    underline = '\033[4m'